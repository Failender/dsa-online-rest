package de.failender.dsaonline.rest.dto;

public class HeldMobilInformation {

    private int asp;
    private int lep;
    private int maxLep;
    private int maxAsp;

    public int getAsp() {
        return asp;
    }

    public void setAsp(int asp) {
        this.asp = asp;
    }

    public int getLep() {
        return lep;
    }

    public void setLep(int lep) {
        this.lep = lep;
    }

    public int getMaxLep() {
        return maxLep;
    }

    public void setMaxLep(int maxLep) {
        this.maxLep = maxLep;
    }

    public int getMaxAsp() {
        return maxAsp;
    }

    public void setMaxAsp(int maxAsp) {
        this.maxAsp = maxAsp;
    }
}
